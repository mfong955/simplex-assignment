"""Task 1 test: construct and inspect the non-ergodic Mess3 dataset.

Run with:
    python test_task1_dataset.py

What this verifies:
1. We can build 3 Mess3 processes with different parameters using fwh_core.
2. We can combine them into a single non-ergodic HMM using the package.
3. We can generate sequences and confirm each sequence comes entirely from one source.
"""

import jax
import jax.numpy as jnp
import numpy as np

from fwh_core.generative_processes.builder import (
    build_nonergodic_hidden_markov_model,
    build_transition_matrices,
)
from fwh_core.generative_processes.transition_matrices import HMM_MATRIX_FUNCTIONS

# ---------------------------------------------------------------------------
# 1. Define the three Mess3 sources
#    Each sequence in training will be generated by exactly one of these.
# ---------------------------------------------------------------------------
SOURCES = [
    {"name": "mess3", "params": {"a": 0.95, "x": 0.05}},  # Source 0: highly structured
    {"name": "mess3", "params": {"a": 0.60, "x": 0.15}},  # Source 1: moderate (paper params)
    {"name": "mess3", "params": {"a": 0.30, "x": 0.25}},  # Source 2: noisy
]
K = len(SOURCES)

# ---------------------------------------------------------------------------
# 2. Build the non-ergodic HMM
#    - Each source contributes 3 hidden states (Mess3 has 3 states).
#    - The composite HMM has K*3 = 9 hidden states total.
#    - The initial state puts equal weight (1/K each) on each source's stationary dist.
#    - Because the transition matrices are block-diagonal, a sequence started in
#      source k's block NEVER crosses into another source's block — non-ergodic.
# ---------------------------------------------------------------------------
hmm = build_nonergodic_hidden_markov_model(
    process_names=[s["name"] for s in SOURCES],
    process_params=[s["params"] for s in SOURCES],
    process_weights=[1.0, 1.0, 1.0],  # equal mixing weights
)

print(f"Transition matrix shape: {hmm.transition_matrices.shape}")
# Expected: (3, 9, 9) — 3 observations, 9 composite hidden states

# ---------------------------------------------------------------------------
# 3. Check block-diagonal structure
#    If the process is truly non-ergodic, T[obs] must be block-diagonal:
#    no probability mass flows between source blocks.
# ---------------------------------------------------------------------------
T = np.array(hmm.transition_matrices)  # (vocab, states, states)
n_states_per_source = T.shape[1] // K

print(f"\nBlock-diagonal check (off-diagonal blocks should be ~0):")
for obs in range(3):
    for i in range(K):
        for j in range(K):
            if i == j:
                continue
            block = T[obs,
                      i * n_states_per_source:(i + 1) * n_states_per_source,
                      j * n_states_per_source:(j + 1) * n_states_per_source]
            print(f"  obs={obs}, source {i}→{j}: max={block.max():.6f}  (should be 0)")

# ---------------------------------------------------------------------------
# 4. Generate a small batch of sequences and confirm they stay in one source
# ---------------------------------------------------------------------------
SEQ_LEN = 20
BATCH_SIZE = 6

key = jax.random.PRNGKey(42)
# We need to split the initial state per sequence
initial_states = jnp.stack([hmm.initial_state] * BATCH_SIZE)  # (B, 9)

keys = jax.random.split(key, BATCH_SIZE)
_, tokens = hmm.generate(initial_states, keys, SEQ_LEN, False)
tokens_np = np.array(tokens)  # (B, SEQ_LEN)

print(f"\nGenerated {BATCH_SIZE} sequences of length {SEQ_LEN}.")
print("Tokens (each row is one sequence):")
print(tokens_np)
print("\nAll token values in {0,1,2}:", set(tokens_np.flatten().tolist()))
